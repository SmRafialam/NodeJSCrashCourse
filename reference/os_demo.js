const os = require('os');

//platform
console.log(os.platform());

//CPU arch
console.log(os.arch());

//cpu core info
console.log(os.cpus());

//Free Memory
console.log(os.freemem());

//Total Memory
console.log(os.totalmem());

//Home Dir
console.log(os.homedir());

//upTime
console.log(os.uptime());